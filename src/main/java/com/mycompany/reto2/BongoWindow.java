    String bongoImage = "/images/bongoN.png";
    boolean keyPressed;
    
    public void selectBongoImage() {
        int bongoType = 1;
        if (keyPressed) {
            if (bongoType == 1) {
                bongoImage = "/images/bongoL.png";
                bongoType = 2;
            } else {
                bongoImage = "/images/bongoR.png";
                bongoType = 1;
            }
        } else {
            bongoImage = "/images/bongoN.png";
        }
    }
    
     private void detectKey() {
        JFrame frame = new JFrame();
        frame.addKeyListener(new KeyAdapter() {
            @Override
            public void keyPressed(KeyEvent e) {
                keyPressed = true;
                System.out.println("Tecla presionada: " + e.getKeyChar());
            }

            @Override
            public void keyReleased(KeyEvent e) {
                keyPressed = false;
                System.out.println("Tecla liberada: " + e.getKeyChar());
            }
        });
        frame.requestFocusInWindow();
    }

    
    public void addBongo() {
        try {
        URL imageUrl = getClass().getResource(bongoImage);
        if (imageUrl == null) {
            throw new IllegalArgumentException("Â¡Imagen no encontrada!");
        }
        Image image = ImageIO.read(imageUrl);
        JPanel imagePanel = new JPanel() {
            @Override
            protected void paintComponent(Graphics g) {
                super.paintComponent(g);
                if (image != null) {
                    g.drawImage(image, 0, 0, getWidth(), getHeight(), this);
                }
            }
        };
        imagePanel.setPreferredSize(jPanelBongoGif.getPreferredSize());
        jPanelBongoGif.setLayout(new BorderLayout());
        jPanelBongoGif.removeAll();
        jPanelBongoGif.add(imagePanel, BorderLayout.CENTER);
        jPanelBongoGif.revalidate();
        jPanelBongoGif.repaint();
    } catch (Exception ex) {
        ex.printStackTrace();
        }
    }

