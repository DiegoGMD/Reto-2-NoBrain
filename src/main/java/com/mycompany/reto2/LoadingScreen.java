    public void addLogo() {
        try {
        URL imageUrl = getClass().getResource("/images/logoEnd.png");
        if (imageUrl == null) {
            throw new IllegalArgumentException("Â¡Imagen no encontrada!");
        }
        Image image = ImageIO.read(imageUrl);
        JPanel imagePanel = new JPanel() {
            @Override
            protected void paintComponent(Graphics g) {
                super.paintComponent(g);
                if (image != null) {
                    g.drawImage(image, 0, 0, getWidth(), getHeight(), this);
                }
            }
        };
        imagePanel.setPreferredSize(jPanelLogoBongoPhoto3.getPreferredSize());
        jPanelLogoBongoPhoto3.setLayout(new BorderLayout());
        jPanelLogoBongoPhoto3.removeAll();
        jPanelLogoBongoPhoto3.add(imagePanel, BorderLayout.CENTER);
        jPanelLogoBongoPhoto3.revalidate();
        jPanelLogoBongoPhoto3.repaint();
    } catch (Exception ex) {
        ex.printStackTrace();
        }
    }

